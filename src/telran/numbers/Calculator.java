package telran.numbers;

public class Calculator {
	static public int multiply(int op1, int op2) {
		return op1 * op2;
	}
	static public int sum(int op1, int op2) {
		return op1 + op2;
	}
	static public int subtract(int op1, int op2) {
		return op1 - op2;
	}
	static public int divide(int op1, int op2) {
		return op1 / op2;
	}
	static public int remainder(int op1, int op2) {
		return op1 % op2;
	}
}
